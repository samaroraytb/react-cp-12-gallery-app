class Gallery extends Component {
  state = {
    displayImageId: 0,
  }

  changeDisplayImage = uniqueId => {
    this.setState({
      displayImageId: uniqueId,
    })
  }

  render() {
    const {displayImageId} = this.state

    const filterDetailsAsPerState = imagesList.filter(
      eachImage => eachImage.id === displayImageId,
    )
    const arrayDestructure = filterDetailsAsPerState.pop()

    return (
      <div className="main-body-container">
        <div className="main-image-section">
          <img
            className="gallery-main-image"
            src={arrayDestructure.imageUrl}
            alt={arrayDestructure.imageAltText}
          />
        </div>
        <h1 className="nature-photo-heading">Nature Photography</h1>
        <p className="nature-photo-paragraph">Nature Photography by Shubham</p>
        <ul className="thumbnail-image-unorder-container">
          {imagesList.map(eachItemDetail => (
            <ThumbnailItem
              changeDisplayImage={this.changeDisplayImage}
              key={eachItemDetail.id}
              imageDetail={eachItemDetail}
              isActiveImage={eachItemDetail.id === displayImageId}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
